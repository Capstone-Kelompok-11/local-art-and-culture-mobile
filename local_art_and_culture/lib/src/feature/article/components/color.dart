import 'package:flutter/material.dart';
// Untuk menyimpan warna, agar tidak dideklarasikan ulang
// Definisikan kelas GlobalColor untuk menyimpan warna-warna yang dapat digunakan secara global
class GlobalColor {
  // Mendefinisikan variabel statis yang merepresentasikan warna kuning tertentu
  static Color yellow = const Color(0xffF3B502);

  // Mendefinisikan variabel statis yang merepresentasikan warna abu-abu tertentu
  static Color grey = const Color(0xff999999);

  // Mendefinisikan variabel statis yang merepresentasikan warna biru-abu tertentu
  static Color blueGrey = const Color(0xff828282);

  // Mendefinisikan variabel statis yang merepresentasikan warna abu-abu terang tertentu
  static Color lightGrey = const Color(0xffB3B3B3);

  // Mendefinisikan variabel statis yang merepresentasikan warna merah tertentu
  static Color red = const Color(0xffE8644B);
}
